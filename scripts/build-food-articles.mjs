#!/usr/bin/env node
/**
 * 從 data/food-articles.json + templates/food-article.html
 * 產生各篇 food-*.html（保留乾淨網址 + 靜態 SEO meta）
 *
 * 用法：node scripts/build-food-articles.mjs
 */

import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const SITE = 'https://tripick.co';

const REGION_EN = { athens: 'Athens', crete: 'Crete', santorini: 'Santorini' };

const HERO_TITLES = {
  athens: '雅典好吃餐廳',
  'crete-chania': '哈尼亞・Loutro 美食',
  'crete-rethymno': '雷希姆龍美食',
  'crete-ierapetra': '伊拉佩特拉美食',
  'santorini-fira': '費拉美食',
  'santorini-oia': '伊亞美食',
};

function esc(str) {
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/"/g, '&quot;')
    .replace(/</g, '&lt;');
}

function ogImage(url) {
  if (!url) return '';
  const u = new URL(url.includes('://') ? url : `${SITE}/${url}`);
  u.searchParams.set('w', '1200');
  u.searchParams.set('q', '80');
  return u.toString();
}

function heroImage(url) {
  if (!url) return '';
  const u = new URL(url.includes('://') ? url : `${SITE}/${url}`);
  u.searchParams.set('w', '1800');
  u.searchParams.set('q', '80');
  return u.toString();
}

function buildJsonLd(article, canonical) {
  return JSON.stringify({
    '@context': 'https://schema.org',
    '@type': 'Article',
    headline: article.title,
    description: article.meta_description || article.blurb,
    image: ogImage(article.image),
    author: { '@type': 'Organization', name: 'Tripick' },
    publisher: { '@type': 'Organization', name: 'Tripick' },
    mainEntityOfPage: canonical,
    inLanguage: 'zh-Hant',
  });
}

function fillTemplate(template, vars) {
  return template.replace(/\{\{(\w+)\}\}/g, (_, key) => vars[key] ?? '');
}

const template = readFileSync(join(ROOT, 'templates/food-article.html'), 'utf8');
const articles = JSON.parse(readFileSync(join(ROOT, 'data/food-articles.json'), 'utf8'));

for (const article of articles) {
  const canonical = `${SITE}/${article.page}`;
  const metaDesc = article.meta_description || article.blurb;
  const pageTitle = `${article.title}｜Tripick 希臘`;
  const ogTitle = `${article.title}｜Tripick 希臘`;
  const regionEn = REGION_EN[article.region_key] || '';
  const heroRegion = `${article.region_zh} ${regionEn}・${article.area_zh} ${article.area_en}`;
  const heroTitle = article.hero_title || HERO_TITLES[article.id] || article.title;

  const html = fillTemplate(template, {
    TITLE: esc(pageTitle),
    CANONICAL: esc(canonical),
    META_DESCRIPTION: esc(metaDesc),
    OG_TITLE: esc(ogTitle),
    OG_DESCRIPTION: esc(metaDesc),
    OG_IMAGE: esc(ogImage(article.image)),
    JSON_LD: buildJsonLd(article, canonical),
    HERO_IMAGE: esc(heroImage(article.hero || article.image)),
    HERO_REGION: esc(heroRegion),
    HERO_TITLE: esc(heroTitle),
    HERO_TITLE_EN: esc(article.title_en),
    FOOD_ARTICLE_ID: esc(article.id),
  });

  const banner = `<!-- AUTO-GENERATED by scripts/build-food-articles.mjs — 請改 data/food-articles.json 後重新執行 build -->\n`;
  writeFileSync(join(ROOT, article.page), banner + html, 'utf8');
  console.log(`✓ ${article.page}`);
}

console.log(`\n已產生 ${articles.length} 篇美食文章 HTML。`);
